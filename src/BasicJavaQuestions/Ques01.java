package BasicJavaQuestions;


   //**********Arithmetic Operators**********//
public class Ques01 {
    public static void main(String[] args) {
        int a =10;
        System.out.println(a); //10
        System.out.println(a++); //10--post_increment => (print, increase)
        System.out.println(++a); //12
        System.out.println(a--); //12
        System.out.println(--a); //10
        System.out.println(a); //10
    }
}
